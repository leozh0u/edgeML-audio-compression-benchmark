"""
Generate the on-device mel filterbank table for esp32/src/mel_frontend.cc.

Mel filters are fixed for a given (sr, n_fft, n_mels) — they don't depend on
weights or training data — so instead of building them on the MCU, we compute
the exact same matrix librosa uses at training time (librosa.filters.mel) once
on the host and ship it as a const C table. Each filter is a contiguous
triangular range over the rfft bins (verified below), so the table is stored
sparse: per-filter (start_bin, weights[]) instead of a dense 64x1025 matrix.
That's a 33x size cut (1990 nonzero entries vs 64*1025).

Run with the esc50env python (needs librosa):
    ../esc50env/bin/python3 scripts/gen_mel_filterbank.py
Writes esp32/src/mel_filterbank_data.h. Deterministic given (SR, N_FFT,
N_MELS) in common.py — do not hand-edit the generated header.
"""

import numpy as np
import librosa

from common import SR, N_MELS, REPO_ROOT

# Not in common.py (wav_to_mel relies on librosa's melspectrogram defaults);
# mirrors esp32/src/config.h N_FFT/HOP_LENGTH — keep in sync if either changes.
N_FFT = 2048

OUT_PATH = REPO_ROOT / "esp32" / "src" / "mel_filterbank_data.h"


def main():
    mel = librosa.filters.mel(sr=SR, n_fft=N_FFT, n_mels=N_MELS)  # (N_MELS, N_FFT//2+1)
    assert mel.shape == (N_MELS, N_FFT // 2 + 1)

    starts, weights_per_filter = [], []
    for row in mel:
        nz = np.nonzero(row)[0]
        if len(nz) == 0:
            starts.append(0)
            weights_per_filter.append(np.array([], dtype=np.float32))
            continue
        lo, hi = nz[0], nz[-1]
        assert np.array_equal(nz, np.arange(lo, hi + 1)), "filter is not contiguous"
        starts.append(int(lo))
        weights_per_filter.append(row[lo:hi + 1].astype(np.float32))

    flat_weights = np.concatenate(weights_per_filter)
    offsets = np.cumsum([0] + [len(w) for w in weights_per_filter[:-1]])
    counts = [len(w) for w in weights_per_filter]

    lines = []
    lines.append("// AUTO-GENERATED by scripts/gen_mel_filterbank.py — do not hand-edit.")
    lines.append(f"// librosa.filters.mel(sr={SR}, n_fft={N_FFT}, n_mels={N_MELS}), Slaney scale/norm (librosa defaults).")
    lines.append("// Sparse triangular filterbank: filter i covers rfft bins")
    lines.append("// [MEL_START_BIN[i], MEL_START_BIN[i] + MEL_NUM_BINS[i]).")
    lines.append("#pragma once")
    lines.append("#include <stdint.h>")
    lines.append("")
    lines.append(f"#define MEL_FILTER_NNZ {len(flat_weights)}")
    lines.append("")
    lines.append("static const float MEL_WEIGHTS[MEL_FILTER_NNZ] = {")
    for i in range(0, len(flat_weights), 8):
        chunk = flat_weights[i:i + 8]
        lines.append("  " + ", ".join(f"{w:.8e}f" for w in chunk) + ",")
    lines.append("};")
    lines.append("")
    lines.append(f"static const uint16_t MEL_START_BIN[{N_MELS}] = {{")
    lines.append("  " + ", ".join(str(s) for s in starts) + ",")
    lines.append("};")
    lines.append("")
    lines.append(f"static const uint16_t MEL_NUM_BINS[{N_MELS}] = {{")
    lines.append("  " + ", ".join(str(c) for c in counts) + ",")
    lines.append("};")
    lines.append("")
    lines.append(f"static const uint32_t MEL_WEIGHT_OFFSET[{N_MELS}] = {{")
    lines.append("  " + ", ".join(str(o) for o in offsets) + ",")
    lines.append("};")
    lines.append("")

    OUT_PATH.write_text("\n".join(lines))
    print(f"wrote {OUT_PATH} ({len(flat_weights)} nonzero weights, "
          f"{len(flat_weights) * 4 + N_MELS * 8} bytes vs {N_MELS * (N_FFT // 2 + 1) * 4} dense)")


if __name__ == "__main__":
    main()
