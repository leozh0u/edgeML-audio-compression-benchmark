"""
Generate esp32/src/test_clips.h: real ESC-50 fold-5 clips embedded as int16 PCM
for the on-device pipeline self-test (BRINGUP_MODE=2). Each clip is run through
mel_frontend_compute -> int8 inference on the board; the firmware asserts the
predicted class matches EXPECT (see main.cpp MODE_SELFTEST).

The two defaults (dog, church_bells) are both classified correctly on-device by
tiny_student_int8_clean.tflite through the exact C mel front end. Run:
  ../esc50env/bin/python3 scripts/gen_test_clips.py
Regenerate if you change the picks or the audio config. Output is gitignored
(1 MB); the firmware needs it only for the self-test build.
"""
from pathlib import Path
import numpy as np
import librosa
import common  # sibling module: paths + audio config

# (ESC-50 category, expected class index). Distinctive + tiny-model-correct.
PICKS = [("dog", 0), ("church_bells", 46)]
OUT = common.REPO_ROOT / "esp32" / "src" / "test_clips.h"


def load_pcm_int16(filename):
    """5s wav -> int16 PCM matching mel_frontend.cc's pcm/32768.0f dequant."""
    y, _ = librosa.load(str(common.AUDIO_DIR / filename), sr=common.SR,
                        duration=common.DURATION)
    if len(y) < common.TARGET_LEN:
        y = np.pad(y, (0, common.TARGET_LEN - len(y)))
    else:
        y = y[:common.TARGET_LEN]
    return np.clip(np.round(y * 32768.0), -32768, 32767).astype("<i2")


def main():
    import pandas as pd
    df = pd.read_csv(common.META_CSV)
    f5 = df[df["fold"] == 5]

    lines = ["#pragma once",
             "// AUTO-GENERATED by scripts/gen_test_clips.py — do not hand-edit.",
             "// Real ESC-50 fold-5 clips as int16 PCM (22050 Hz, 5 s) for the",
             "// on-device pipeline self-test (mel_frontend -> int8 inference).",
             "#include <stdint.h>",
             '#include "config.h"',
             "",
             f"#define N_TEST_CLIPS {len(PICKS)}",
             ""]
    names, expects = [], []
    for cat, cls in PICKS:
        row = f5[f5["category"] == cat].iloc[0]
        pcm = load_pcm_int16(row["filename"])
        assert pcm.shape[0] == common.TARGET_LEN, pcm.shape
        var = f"clip_{cat}"
        names.append(var); expects.append(cls)
        lines.append(f"// {cat} ({row['filename']}): expected class index {cls}")
        lines.append(f"static const int16_t {var}[CLIP_SAMPLES] = {{")
        for i in range(0, len(pcm), 20):
            lines.append("  " + ",".join(str(int(v)) for v in pcm[i:i + 20]) + ",")
        lines.append("};")
        lines.append("")
        print(f"{cat:14} target={cls:2}  {row['filename']}")
    lines.append(f"static const int16_t* const TEST_CLIP_PCM[N_TEST_CLIPS] = "
                 f"{{ {', '.join(names)} }};")
    lines.append(f"static const int TEST_CLIP_EXPECT[N_TEST_CLIPS] = "
                 f"{{ {', '.join(map(str, expects))} }};")
    lines.append("")
    OUT.write_text("\n".join(lines))
    print(f"wrote {OUT} ({OUT.stat().st_size/1024:.0f} KB, {len(PICKS)} clips)")


if __name__ == "__main__":
    main()
